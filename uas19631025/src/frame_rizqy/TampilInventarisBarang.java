    public ArrayList<InventarisBarang> getInventarisBarangList(String keyword){
        ArrayList<InventarisBarang> inventarisbarangList = new ArrayList<InventarisBarang>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();
        
        String query = "SELECT * FROM trx_inventaris_barang "+keyword;
        Statement st;
        ResultSet rs;
        
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()){
                inventarisbarang = new InventarisBarang(
                    rs.getInt("kd_inventaris_barang"),
                    rs.getString("kd_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("merk"),
                    rs.getString("jumlah"),
                    rs.getString("lantai"),
                    rs.getString("nama_ruang"));
            inventarisbarangList.add(inventarisbarang);
            }
        } catch (SQLException | NullPointerException ex) {
            System.err.println("Koneksi Null Gagal");
        }
        return inventarisbarangList;
    }
    
    public void selectInventarisBarang(String keyword){
        ArrayList<InventarisBarang> list = getInventarisBarangList(keyword);
        DefaultTableModel model = (DefaultTableModel)jInventarisBarang.getModel();
        Object[] row = new Object[7];
        
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getKdInventarisBarang();
            row[1] = list.get(i).getKdBarang();
            row[2] = list.get(i).getNamaBarang();
            row[3] = list.get(i).getMerk();
            row[4] = list.get(i).getJumlah();
            row[5] = list.get(i).getLantaiRuang();
            row[6] = list.get(i).getNamaRuang();
            
            model.addRow(row);
        }
    }
    
    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)jInventarisBarang.getModel();
        model.setRowCount(0);
        selectInventarisBarang(keyword);
    }

